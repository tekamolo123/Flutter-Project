class Tour {
  final String id;
  final String title;
  final String country;
  final String city;
  final int days;
  final double price;
  final String description;

  const Tour({
    required this.id,
    required this.title,
    required this.country,
    required this.city,
    required this.days,
    required this.price,
    required this.description,
  });
}
